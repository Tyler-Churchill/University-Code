#pragma once

/// \brief class Attribute
///
class Attributes
{
  public:

   /// \brief Gets the current strength value
   ///
   /// \return Returns the current strength value
   int const getStrength()  { return strength; }

   /// \brief Gets the current vitality value
   ///
   /// \return Returns the current vitality value
   int const getVitality() { return vitality; }

   /// \breif Gets the current agility value
   ///
   /// \return Returns the current agility value
   int const getAgility() { return agility; }

   /// \brief Gets the current health points value
   ///						
   /// \return Returns the current health points value
   int const getHealthPoints() { return healthPoints; }

   /// \brief Gets the current armor value
   ///
   /// \return Returns the current armor value
   int const getArmor() { return armor; }

   /// \brief Gets the current damage value
   ///
   /// \return Returns the current damage value
   int const getDamage() { return damage; }
  /// \brief Sets the strength value
   ///
   /// \param[in] Strength value to be set
   void setStrength(int x) { strength = x; }
   /// \brief Sets the vitality value
   ///
   /// \param[in] vitality value to be set
   void setVitality(int x) { vitality = x; }
   /// \brief Sets the Agility value
   ///
   /// \param[in] Agility value to be set
   void setAgility(int x) { agility = x; }
   /// \brief Sets the Health Points value
   ///
   /// \param[in] Health Points value to be set
   void setHealthPoints(int x) { healthPoints = x; }
    /// \brief Sets the Armor value
   ///
   /// \param[in] Armor value to be set
   void setArmor(int x) { armor = x; }
    /// \brief Sets the Damage value
   ///
   /// \param[in] Damage value to be set
   void setDamage(int x) { damage = x; }
   
  private:
   /// \brief Strength Stat
   int strength = 0;
   /// \brief Vitality Stat
   int vitality = 0;
   /// \brief Agility Stat
   int agility = 0;
   /// \brief Health Points Stat
   int healthPoints = 0;
   /// \brief Armor Stat
   int armor = 0;
   /// \brief Damage Stat
   int damage = 0;
};
